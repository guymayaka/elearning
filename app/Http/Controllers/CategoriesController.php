    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($slug)
    {
        $categorie = ArticlesCategory::where('slug', $slug)->first();
        $articles = Article::orderBy('id', 'desc')->where('category_id', $categorie->id)->where('published', 1)->paginate(8);
        $publications = Publication::orderBy('id', 'desc')->where('published', 1)->limit(8)->get();
      
        return view('elearning.category-detail', compact('categorie', 'articles', 'publications'));
    }

